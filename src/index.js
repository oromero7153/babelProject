const multiply = (a,b) => a * b
const firstNum = 2
const secondNum = 3
let product = multiply(firstNum, secondNum)
console.log(`The product of ${firstNum} and ${secondNum} is ${product}`);

const sum = (a,b) => a + b
const firstAdd = 5
const secondAdd = 15
let total = sum(firstAdd, secondAdd)
console.log(`The total of ${firstAdd} and ${secondAdd} is ${total}`);